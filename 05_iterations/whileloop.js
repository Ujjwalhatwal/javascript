// while loop

let index = 0;
while (index <= 10) {
    console.log(`value of index is ${index}`);   
    index = index + 3;
}

let myArray  = [1,2,3,4,5,6]
let arr = 0
while (arr < myArray.length) {
    console.log(`value of array at ${arr} is ${myArray[arr]}`);   
    arr++
}

// do while loop
let score = 11;
do {
    console.log(`score is ${score}`);
    score++
} while (score < 4);

